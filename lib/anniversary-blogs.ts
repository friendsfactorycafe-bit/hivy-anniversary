// Helper function to get all anniversary blog posts
export function getAllAnniversaryBlogs(): BlogPost[] {
  return anniversaryBlogPosts.sort((a, b) => 
    new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

// Helper function to get blog by slug
export function getAnniversaryBlogBySlug(slug: string): BlogPost | undefined {
  return anniversaryBlogPosts.find(b => b.slug === slug);
}
